#include <iostream>

int main() {
    // Greet 3 of your classmates with this program, in three separate lines
    // like:
    //
    // Hello, Esther!
    // Hello, Mary!
    // Hello, Joe!

    // Solution: Used two ways to add end line.

    std::cout << "Hello, Esther!\n";
    std::cout << "Hello, Mary!" << std::endl;
    std::cout << "Hello, Joe!\n";

    return 0;
}